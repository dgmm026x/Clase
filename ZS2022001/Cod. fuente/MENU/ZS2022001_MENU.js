function BURGUERMENU()
{
  var STATUS=document.getElementById("BURGUERMENU");
  var WIDTH=window.width;

  if(WIDTH>=900)
  {
    STATUS.style.display="none";}
  else
  {
    if(STATUS.style.display==="block"){STATUS.style.display="none";}

    else{STATUS.style.display="block";}
  }
}



