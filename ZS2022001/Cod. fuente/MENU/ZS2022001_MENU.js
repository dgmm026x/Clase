{
  var STATUS = document.getElementById("BurguerWindow");
  // var WIDTH = display.width;


  if(STATUS.style.display === "block")
  {
    STATUS.style.display = "none";
  }


  else
  {
    STATUS.style.display = "block";
  }
}



